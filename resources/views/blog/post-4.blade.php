										<p>
																					
											<h4> ¿Qué hago si tengo un accidente?</h4>

											Mantén la calma, cerciórate y toma las precauciones necesarias para que ni tú ni tu vehículo corran riesgos innecesarios, como por ejemplo un nuevo accidente. Llama inmediatamente a la aseguradora y a tu corredor de seguros, en donde te darán la asesoría adecuada.
											Si hubiera terceros involucrados, abstente de negociar hasta que llegue el representante de la aseguradora. <br>
											Dependiendo de la gravedad del accidente y la presencia de terceros involucrados, deberás formalizar tus declaraciones por escrito con uno de los asesores de la aseguradora, efectuar una denuncia en la comisaría del sector y someterte al dosaje etílico de ley. Además, de no poder movilizar tu vehículo, podrás solicitar una grúa.

										</p>

										 <p>

											<h4> ¿Qué hago si choqué con alguien?</h4>

											Sigue las mismas indicaciones de la pregunta anterior. Además, abstente de negociar o comprometerte con la otra persona involucrada en el accidente hasta que llegue el procurador.

										</p>	

										<p>

											<h4>  ¿Qué pasa en caso de atropello?</h4>

											Sigue las mismas indicaciones de la pregunta anterior. Además, abstente de negociar o comprometerte con la otra persona involucrada en el accidente hasta que llegue el procurador.

										</p>

										<p>

											<h4>  ¿Qué pasa si me roban el auto?</h4>

											Llama inmediatamente a tu aseguradora y efectúa la denuncia policial en la comisaría del sector. En caso que el vehículo cuente con algún dispositivo de rastreo o GPS, llama inmediatamente al proveedor del GPS y a la aseguradora (los plazos máximos dependen de cada aseguradora, pero normalmente son entre 2 y 4 horas).<br>
											Adicionalmente, dentro de los 2 días hábiles siguientes al robo, tienes que acercarte a la plataforma de atención de siniestros de vehículos de la aseguradora, para regularizar la denuncia mediante declaración escrita.


