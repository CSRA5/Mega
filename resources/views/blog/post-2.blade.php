										<p>Al momento de comprar nuestro primer auto nos vemos ante la necesidad de asegurarlo, en el mercado peruano un propietario de auto tiene dos tipos de seguro para adquirir, el SOAT (Seguro Obligatorio de Accidentes de Tránsito) y el Seguro Vehicular. Entender las diferencias entre ambos es importante para entender cuando son necesarios y que rol cumplen en nuestra protección. <br>
										Muchos se preguntarán ¿No son lo mismo? La realidad nos dice lo contrario, no estamos hablando del mismo producto. Es por eso que en esta entrada analizaremos las diferencias entre un seguro vehicular y el SOAT. <br>
										El Seguro Obligatorio contra Accidentes de Tránsito (SOAT) es, como lo dice su nombre, un seguro obligatorio que todo vehículo motorizado debe tener para circular a nivel nacional y que en de ocurrir un accidente protegerá a los afectados por el mismo. ya sea que se trate de lesiones o su fallecimiento. El SOAT cubre a todas las personas afectadas por un accidente ya que cubre tanto a los ocupantes del auto como a terceros afectados.
										
										<h4>¿Qué cubre el SOAT?</h4>
										El SOAT se encarga de cubrir gastos relacionados con el bienestar de los afectados. Es decir, cubre gastos médicos (quirúrgicos, farmacéuticos y hospitalarios), da una indemnización por invalidez permanente, muerte; y además, cubre gastos funerarios y gastos de transporte de las víctimas a hospitales, entre otros. <br><br>

										<h4> ¿Y el Seguro Vehicular?</h4>
										 El Seguro Vehicular es una póliza más completa y que cubre en exceso a lo que cubre el SOAT. Pero además, el Seguro Vehicular cubre los daños producidos a tu vehículo. Esto quiere decir que el Seguro Vehicular contra todo riesgo te cubre contra: daños sufridos por el vehículo, robos, incendios, vandalismo y cualquier otro daño que tu vehículo pueda sufrir. <br>
										 Además algunas pólizas de Seguro Vehicular cubren gastos legales, la pérdida total del vehículo y los posibles daños que un accidente pueda ocasionar a los bienes públicos o privados como semáforos, kioskos, etc.
										 Los beneficios que te ofrece un Seguro Vehicular dependen de la póliza y empresa con la que se contrate por eso es muy importante que compares antes de elegir y selecciones la póliza con las mejores coberturas para tu caso en particular. <br>
										 Uno no reemplaza al otro, sino que ambos productos se complementan para la protección de los pasajeros y del vehículo. El SOAT protege a los que te importan y el seguro vehicular además protege tu vehículo.

